#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	unsigned char	*us1;
	unsigned char	*us2;

	i = 0;
	us1 = (unsigned char *)s1;
	us2 = (unsigned char *)s2;
	while (us1[i] && us2[i] && us1[i] == us2[i] && i < n - 1)
		i++;
	if (n < 1)
		return (0);
	return (us1[i] - us2[i]);
}
