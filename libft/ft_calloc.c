#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*ptr;

	if (((long)nmemb != 0 && (long)size != 0 && ((long)size < 0
				|| (long)nmemb < 0)))
	{
		return (0);
	}
	ptr = (void *)malloc(nmemb * size);
	if (!ptr)
		return (NULL);
	ft_bzero(ptr, (nmemb * size));
	return (ptr);
}
